package apilapes.com.br.apilapes.modelo;

public class Pessoa {
    //atributos
    private String nome;
    private int idade;

    //get e set
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
   
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
